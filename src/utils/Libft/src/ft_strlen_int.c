#include "../headers/libft.h"

size_t	ft_strlen_int(const char *s)
{
	size_t	i;
	size_t	j;

	i = 0;
	j = 0;
	if (s[i] == '-' || s[i] == '+')
		j++;
	while (s[i] != '\0')
		i++;
	return (i - j);
}
